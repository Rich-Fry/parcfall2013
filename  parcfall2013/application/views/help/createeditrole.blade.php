<h2>Create/Edit User Roles</h2>
<br><br><br><br>

<img src="/img/Role1.PNG"/>
<br><br>
<p>
	Click “Users” link in the upper right corner of any page.
	<br><br><br><br>
</p>

<img src="/img/Role2.PNG"/>
<br><br>
<p>
	Click “Manage System Roles” button.
	<br><br><br><br>
</p>

<h4>To Create</h4>
<br><br><br><br>

<img src="/img/Role3.PNG"/>
<br><br>
<p>
	Click “Create” button.
	<br><br><br><br>
</p>

<img src="/img/Role4.PNG"/>
<br><br>
<p>
	In popup, fill in name, description, click permissions allowed for role. Then click “Create” button.
	<br><br><br><br>
</p>

<h4>To Edit</h4>
<br><br><br><br>

<img src="/img/Role5.PNG"/>
<br><br>
<p>
	Click desired role. Selection will be blue.
	<br><br><br><br>
</p>

<img src="/img/Role6.PNG"/>
<br><br>
<p>
	Click “Edit” button.
	<br><br><br><br>
</p>

<img src="/img/Role7.PNG"/>
<br><br>
<p>
	Change desired information, click “Save” button.
	<br><br><br><br>
</p>


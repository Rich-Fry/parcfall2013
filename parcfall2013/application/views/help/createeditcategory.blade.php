<h2>Create/Edit Personnel Record Categories</h2>
<br><br><br><br>

<img src="/img/Cat1.PNG"/>
<br><br>
<p>
	Click the Categories link in the upper right corner of any page..
	<br><br><br><br>
</p>

<h4>To Create</h4>
<br><br><br><br>

<img src="/img/Cat2.PNG"/>
<br><br>
<p>
	Click “Create” button.
	<br><br><br><br>
</p>

<img src="/img/Cat3.PNG"/>
<br><br>
<p>
	In the pop-up window, give the category a name and brief description of its purpose.<br>
	Then click “Create” button.
	<br><br><br><br>
</p>

<img src="/img/Cat4.PNG"/>
<br><br>
<p>
	Select the newly created category. Selected line will be blue.
	<br><br><br><br>
</p>

<img src="/img/Cat5.PNG"/>
<br><br>
<p>
	Click “Create/Edit Template” button.
	<br><br><br><br>
</p>

<img src="/img/Cat6.PNG"/>
<br><br>
<p>
	In popup, add and name desired fields to the category, then save the template.
	<br><br><br><br>
</p>

<h4>To Edit</h4>
<br><br><br><br>

<img src="/img/Cat7.PNG"/>
<br><br>
<p>
	Select the desired category. Selected line will be blue.
	<br><br><br><br>
</p>

<img src="/img/Cat8.PNG"/>
<br><br>
<p>
	Click “Edit” button.
	<br><br><br><br>
</p>


<img src="/img/Cat9.PNG"/>
<br><br>
<p>
	In popup, you may change the name and description, then save the category.
	<br><br><br><br>
</p>

<h4>Additional Features</h4>
<br><br><br><br>

<img src="/img/Cat10.PNG"/>
<br><br>
<p>
	Select the desired category. Selected line will be blue.
	<br><br><br><br>
</p>

<img src="/img/Cat11.PNG"/>
<br><br>
<p>
	To view the basic information of the category without changing it, click “View” button.
	<br><br><br><br>
</p>

<img src="/img/Cat12.PNG"/>
<br><br>
<p>
	To view the fields in the category without changing them, click “View Template” button.
	<br><br><br><br>
</p>

<img src="/img/Cat13.PNG"/>
<br><br>
<p>
	If category is no longer in use, click “Archive” button to send it to archived list.<br>
	“Unarchive” button is on archived tab to move roles back to active list.
</p>
<h2 style="text-align: center;">Common Terms</h2>

<p><strong style="font-size: 110%;">Client - </strong> Someone who PARC helps find employment at other companies</p>

<p><strong style="font-size: 110%;">Employee - </strong> Someone who works for PARC and is paid by them</p>

<p><strong style="font-size: 110%;">Programs - </strong> What part of PARC a client or employee is a part of. For instance Human Recourses or Supervisor.</p>

<p><strong style="font-size: 110%;">Roles - </strong>  What part a user plays on Speed 3.0 weather they are a system administrator or simply a report 
	generator. A role determines what permissions a user has on the site.m</p>

<p><strong style="font-size: 110%;">User - </strong> Someone who uses and has an account on Speed 3.0</p>
